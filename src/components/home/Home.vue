<template>
  <div>
    <h1 v-meu-transform="15" class="titulo centralizado">{{ titulo }}</h1>
    <p v-show="mensagem" class="centralizado">{{ mensagem }}</p>
    <input
      v-on:input="filtro = $event.target.value"
      type="text"
      class="filtro"
      placeholder="filtre pelo titulo"
    />
    <ul class="lista-fotos">
      <li
        class="lista-fotos-item"
        v-for="foto in fotosComFiltro"
        v-bind:key="foto"
      >
        <meu-painel :titulo="foto.titulo">
          <minha-img
            v-meu-transform.animate.reverse="15"
            :url="foto.url"
            :titulo="foto.titulo"
          ></minha-img>
          <router-link :to="{name: 'altera', params: { id: foto._id}}">
            <meu-botao tipo="button" rotulo="Alterar"></meu-botao>
          </router-link>
          <meu-botao
            @botaoAtivado="remover(foto)"
            tipo="button"
            rotulo="Remover"
            estilo="perigo"
          ></meu-botao>
        </meu-painel>
      </li>
    </ul>
  </div>
</template>

<script>
import ImagemResponsive from "../imagem-responsiva/ImagemResponsive.vue";
import Botao from "../shared/botao/Botao.vue";
import Painel from "../shared/painel/Painel.vue";
import FotoService from "../../components/domain/foto/FotoService";

export default {
  data() {
    return {
      titulo: "AluraPic",
      fotos: [],
      filtro: "",
      mensagem: "",
    };
  },
  components: {
    "meu-painel": Painel,
    "minha-img": ImagemResponsive,
    "meu-botao": Botao,
  },

  computed: {
    fotosComFiltro() {
      if (this.filtro != "") {
        let exp = new RegExp(this.filtro.trim(), "i");
        return this.fotos.filter((foto) => exp.test(foto.titulo));
      } else {
        return this.fotos;
      }
    },
  },

  methods: {
    remover(foto) {
      this.service.apaga(foto._id).then(
        () => {
          let indice = this.fotos.indexOf(foto);
          this.fotos.splice(indice, 1);
          this.mensagem = "Foto removida com sucesso";
        },
        (err) => {
          this.mensagem = err.message
        }
      );
    },
  },

  created() {
    this.service = new FotoService(this.$resource);

    this.service.lista().then(
      (fotos) => (this.fotos = fotos), err => this.mensagem = err.message)
  }
}
</script>


<style scoped>
h1.centralizado {
  text-align: center;
}

ul.lista-fotos {
  list-style: none;
}

ul.lista-fotos li.lista-fotos-item {
  display: inline-block;
}

input.filtro {
  display: block;
  width: 100%;
}
</style>
